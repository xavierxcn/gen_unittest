
package com.example.calculator;

/**
 * 简单的计算器类，用于演示测试生成
 */
public class Calculator {
    /**
     * 两数相加
     * @param a 第一个数
     * @param b 第二个数
     * @return 两数之和
     */
    public int add(int a, int b) {
        return a + b;
    }
    
    /**
     * 两数相乘
     * @param a 第一个数
     * @param b 第二个数
     * @return 两数之积
     */
    public int multiply(int a, int b) {
        return a * b;
    }
}
